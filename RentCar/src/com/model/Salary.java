package com.model;

public class Salary {
	

	
	private int salID;
	private String NIC;
	
	private float basSal;
	private int attendnce;
	private float EPF; 
	private float ETF;
	private float NetSal;
	private String date;
	
	
	
	
	
	public int getSalID() {
		return salID;
	}
	public void setSalID(int salID) {
		this.salID = salID;
	}
	public String getNIC() {
		return NIC;
	}
	public void setNIC(String nIC) {
		NIC = nIC;
	}
	public float getBasSal() {
		return basSal;
	}
	public void setBasSal(float basSal) {
		this.basSal = basSal;
	}
	public int getAttendnce() {
		return attendnce;
	}
	public void setAttendnce(int i) {
		this.attendnce = i;
	}
	public float getEPF() {
		return EPF;
	}
	public void setEPF(float ePF) {
		EPF = ePF;
	}
	public float getETF() {
		return ETF;
	}
	public void setETF(float eTF) {
		ETF = eTF;
	}
	public float getNetSal() {
		return NetSal;
	}
	public void setNetSal(float netSal) {
		NetSal = netSal;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	
	
	
	
	
	
	

	
	
	
	
	
	
	
	

}
